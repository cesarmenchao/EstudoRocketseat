// Operator Condicionao (Ternario)

// Dependendo de condição, nós rebeberemos valores diferentes

//Condição então valor 1 se não valor 2 valores diferentes

//Condição então valor 1 se não valor 2
//condition ? value1 : value2

// Exemplos

// Café da manhã top
let pao = true;
let queijo = false;
const niceBreakfat = pao && queijo ? "café top" : "café ruim";

console.log(niceBreakfat);

//Maior de 18
let age = 18;
const Maior = age > 18 ? "maior de idade" : "menor de idade";
console.log(Maior);
